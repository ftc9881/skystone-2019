package org.firstinspires.ftc.teamcode.auto.utility;

import android.content.Context;
import android.content.res.Resources;

import com.qualcomm.ftccommon.SoundPlayer;
import com.qualcomm.robotcore.eventloop.opmode.LinearOpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;

import org.firstinspires.ftc.teamcode.Robot;

public class Soundboard {

    // List of available sound resources
    private String sounds[] =  {"ss_alarm", "ss_bb8_down", "ss_bb8_up", "ss_darth_vader", "ss_fly_by",
            "ss_mf_fail", "ss_laser", "ss_laser_burst", "ss_light_saber", "ss_light_saber_long", "ss_light_saber_short",
            "ss_light_speed", "ss_mine", "ss_power_up", "ss_r2d2_up", "ss_roger_roger", "ss_siren", "ss_wookie" };
    private SoundPlayer.PlaySoundParams soundParams;
    private Context context;

    public Soundboard(Robot robot) {
        this.context = robot.fileContext;

        // create a sound parameter that holds the desired player parameters.
        SoundPlayer.PlaySoundParams params = new SoundPlayer.PlaySoundParams();
        params.loopControl = 0;
        params.waitForNonLoopingSoundsToFinish = false;
    }

    public void play(String name) {
        int soundID = context.getResources().getIdentifier(name, "raw", context.getPackageName());
        if (soundID != 0) {
            SoundPlayer.getInstance().startPlaying(context, soundID, soundParams, null,
                new Runnable() {
                    public void run() {
                        // sound finished playing
                    }
                });
        }
    }
}
